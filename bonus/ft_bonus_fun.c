#include"ft_printf.h"

int ft_atoi_flag(char *str, int n)
{
    int n_ci;
    int n_0;

    n_ci = 0;
    while(n >= 10)
    {
        n /=10;
        n_ci++;
        n_0 = ft_atoi(str) - n_ci;
    }
}